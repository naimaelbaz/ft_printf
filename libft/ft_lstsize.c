#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		tmp;
	t_list	*p;

	tmp = 0;
	p = lst;
	while (p != NULL)
	{
		p = p->next;
		tmp++;
	}
	return (tmp);
}
