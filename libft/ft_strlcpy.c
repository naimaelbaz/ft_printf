#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	ln;

	ln = ft_strlen(src);
	if (dstsize == 0)
		return (ln);
	i = 0;
	if (dstsize > 0)
	{
		while (src[i] != '\0' && i < dstsize - 1)
		{
			dst[i] = ((char *)src)[i];
			i++;
		}
	dst[i] = '\0';
	}
	return (ln);
}
